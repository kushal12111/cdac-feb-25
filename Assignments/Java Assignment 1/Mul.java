import java.util.*;
class Mul{
    public static void main(String args[]){
        Scanner sc=new Scanner(System.in);
        System.out.print("Input first number:");
        int a=sc.nextInt();
        System.out.print("Input second number:");
        int b=sc.nextInt();
        int mul=a*b;
        // int final=
        System.out.println(a+"x"+b+"="+mul);
        
    }
}